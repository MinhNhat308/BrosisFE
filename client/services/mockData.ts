// Generate event participations for all students
export const generateEventParticipations = () => {
  const participations = [];
  const statuses = ["attended", "registered", "excuse", "absent", "not_registered"];
  
  mockStudents.forEach(student => {
    mockEvents.forEach(event => {
      const status = statuses[Math.floor(Math.random() * statuses.length)];
      
      participations.push({
        id: participations.length + 1,
        student_id: student.id,
        event_id: event.id,
        participation_status: status,
        registered_at: status !== "not_registered" ? "2024-01-15T10:00:00Z" : null,
        registration_note: status !== "not_registered" ? `Ghi chú cho ${student.name}` : null,
        contact_info: status !== "not_registered" ? student.phone : null,
        event: event
      });
    });
  });
  
  return participations;
};

